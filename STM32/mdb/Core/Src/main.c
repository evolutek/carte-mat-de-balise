void ringbuffer_read_exactly(ringbuffer_t* buffer, uint8_t *data, uint32_t max_size) {
	uint32_t i = 0;

	while (i < max_size) {
		ringbuffer_dma_set_written_size(buffer, 512 - huart1.hdmarx->Instance->CNDTR);
		i += ringbuffer_read(buffer, &data[i], max_size - i);
	}
}

